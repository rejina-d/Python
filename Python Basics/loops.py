#loops in python : for loop, while loop, nested loops

#requirements in loops: (initialization, condition, increment/decrement)

#while loop
i = 1
while i <=10:
    print(i)
    i += 1

#for printing in same line
i = 1
while i <=10:
    print(i, end= " ")
    i += 1
    
#for loop
for j in range(1,11):
    print(j, end= " ")
print(" ")

#nested loops
for k in range(1, 4):
    for l in range(1, 4):
        print(1, end=" ")
    print(" ")
        
    
    